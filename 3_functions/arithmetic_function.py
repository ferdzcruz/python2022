def add_num(n1, n2):
    return f"The sum of {n1} + {n2} is {n1 + n2}"

def subtract_num(n1, n2):
    return f"The difference of {n1} - {n2} is {n1 - n2}"

def multiply_num(n1, n2):
    return f"The product of {n1} x {n2} is {n1 * n2}"

def divide_num(n1, n2):
    return f"The quotient of {n1} / {n2} is {n1 / n2}"



